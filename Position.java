public class Position {
    
    int i,j;
    String queen;
    

    public Position(int i, int j){
        this.i = i;
        this.j = j;
    }


    public int getI() {
        return i;
    }


    public void setI(int i) {
        this.i = i;
    }


    public int getJ() {
        return j;
    }


    public void setJ(int j) {
        this.j = j;
    }


    public String getQueen() {
        return queen;
    }


    public void setQueen(String queen) {
        this.queen = queen;
    }

    
}
